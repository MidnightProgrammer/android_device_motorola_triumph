APN_SQL_PATH=/system/etc/init.apn.sql
TELEPHONY_DB=/data/data/com.android.providers.telephony/databases/telephony.db
TELEPHONE_DATA=/system/etc/telephony.sql

LOG_TAG="APN-Preload"
LOG_NAME="${0}:"

loge ()
{
  /system/bin/log -t $LOG_TAG -p e "$LOG_NAME $@"
}

logi ()
{
  /system/bin/log -t $LOG_TAG -p i "$LOG_NAME $@"
}

failed ()
{
  loge "$1: exit code $2"
  exit $2
}

rm -f /data/data/com.android.providers.telephony/databases/telephony*
echo ".restore $TELEPHONE_DATA" | sqlite3 -init - $TELEPHONY_DB
chown radio $TELEPHONY_DB*
chgrp radio $TELEPHONY_DB*

logi Loaded Virgin Mobile APN Default

exit 0
